  const translateY = useSharedValue(0);

  useEffect(() => {
    if (!animated) return;
    translateY.value = withRepeat(
      withTiming(-height, { duration: scrollDuration, easing: Easing.linear }),
      -1,
      false,
    );
  }, [animated, height, scrollDuration, translateY]);

